export function searchVariations(query: string): VariationHierarchy[] {
  const q = query.toLowerCase();
  return VARIATION_HIERARCHY.filter(v =>
    v.name.toLowerCase().includes(q) ||
    v.style.toLowerCase().includes(q) ||
    v.group.toLowerCase().includes(q)
  );
}

export function filterByGroup(group: string): VariationHierarchy[] {
  return VARIATION_HIERARCHY.filter(v => v.group === group);
}

export function filterByStyle(style: string): VariationHierarchy[] {
  return VARIATION_HIERARCHY.filter(v => v.style === style);
}
